package com.allmuslim.response;

public class SuraResponse {
	
	private int numberofAyyah;
	private String surrahname_malay;
	private String surrahname_english;
	private String surrahname_ownLang;
	public int getNumberofAyyah() {
		return numberofAyyah;
	}
	public void setNumberofAyyah(int numberofAyyah) {
		this.numberofAyyah = numberofAyyah;
	}
	public String getSurrahname_malay() {
		return surrahname_malay;
	}
	public void setSurrahname_malay(String surrahname_malay) {
		this.surrahname_malay = surrahname_malay;
	}
	public String getSurrahname_english() {
		return surrahname_english;
	}
	public void setSurrahname_english(String surrahname_english) {
		this.surrahname_english = surrahname_english;
	}
	public String getSurrahname_ownLang() {
		return surrahname_ownLang;
	}
	public void setSurrahname_ownLang(String surrahname_ownLang) {
		this.surrahname_ownLang = surrahname_ownLang;
	}
	
	

}
